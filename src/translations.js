    const langToggle = document.getElementById('lang-toggle');
    // Check localStorage for saved preference, default to 'es'
    let currentLang = localStorage.getItem('preferredLanguage') || 'es';

    // Function to update content based on selected language
    window.updateLanguage = (lang) => {
        // Update Toggle Text
        if (langToggle) {
            langToggle.innerHTML = lang === 'es' ?
                '<span class="text-brand-accent">ES</span> / EN' :
                'ES / <span class="text-brand-accent">EN</span>';
        }

        // Update Content
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                el.innerHTML = translations[lang][key];
            }
        });

        // Update Placeholders
        const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
        placeholders.forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (translations[lang] && translations[lang][key]) {
                el.placeholder = translations[lang][key];
            }
        });
    };

    // Initialize language on load
    if (currentLang !== 'es') {
        updateLanguage(currentLang);
    }

    // Add event listener to the language toggle button
    if (langToggle) {
        langToggle.addEventListener('click', () => {
            currentLang = currentLang === 'es' ? 'en' : 'es';
            // Save preference
            localStorage.setItem('preferredLanguage', currentLang);
            updateLanguage(currentLang);
        });
    }
